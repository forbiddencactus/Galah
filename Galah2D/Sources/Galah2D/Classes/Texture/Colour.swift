public struct Colour
{
    public let r: Float;
    public let g: Float;
    public let b: Float;
    public let a: Float;
    
    public init(_ red: Float,_ green: Float,_ blue: Float,_ alpha: Float)
    {
        r = red;
        g = green;
        b = blue;
        a = alpha;
    }
}
